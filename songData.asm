;1. Instrument data
;   f ([s s]/[s]) [v] [a] [p p] t
;   f - Flags
;       0sirnvap
;       s - Update sample
;       i - Choose sample by index, not absolute position
;       r - Update sample position relative to current position (works independently of i)
